// Upload avatar function
exports.uploadAvatar = async (req, res) => {
  try {
    const userId = req.user.id; // From auth middleware

    if (!req.file) {
      return res.status(400).json({ error: "No file uploaded" });
    }

    // Update user avatar in database (handle missing column gracefully)
    let updatedUser;
    try {
      updatedUser = await pool.query(
        "UPDATE users SET avatar = $1 WHERE id = $2 RETURNING id, name, email, avatar",
        [req.file.filename, userId]
      );
    } catch (dbError) {
      // If column doesn't exist, add it first
      if (dbError.message.includes('column') && dbError.message.includes('avatar')) {
        console.log('Avatar column missing, adding it...');
        await pool.query('ALTER TABLE users ADD COLUMN avatar VARCHAR(255)');
        
        // Retry the update
        updatedUser = await pool.query(
          "UPDATE users SET avatar = $1 WHERE id = $2 RETURNING id, name, email, avatar",
          [req.file.filename, userId]
        );
      } else {
        throw dbError;
      }
    }

    if (updatedUser.rows.length === 0) {
      return res.status(404).json({ error: "User not found" });
    }

    console.log('Avatar uploaded successfully for user:', userId);

    res.json({
      message: "Profile picture uploaded successfully",
      avatar: `http://localhost:5000/uploads/avatars/${req.file.filename}`
    });

  } catch (error) {
    console.error('Avatar upload error:', error);
    res.status(500).json({ error: "Failed to upload profile picture: " + error.message });
  }
};

// Remove avatar function
exports.removeAvatar = async (req, res) => {
  try {
    const userId = req.user.id; // From auth middleware

    // Get current user to check if they have an avatar
    const currentUser = await pool.query(
      "SELECT avatar FROM users WHERE id = $1",
      [userId]
    );

    if (currentUser.rows.length === 0) {
      return res.status(404).json({ error: "User not found" });
    }

    // Update user avatar to null in database
    await pool.query(
      "UPDATE users SET avatar = NULL WHERE id = $1",
      [userId]
    );

    console.log('Avatar removed successfully for user:', userId);

    res.json({
      message: "Profile picture removed successfully"
    });

  } catch (error) {
    console.error('Avatar removal error:', error);
    res.status(500).json({ error: "Failed to remove profile picture: " + error.message });
  }
};

